#ifndef ssd1803_REG_H_
#define ssd1803_REG_H_

// Register address calculation
#define ssd1803_REG(pos) (1<<pos)

// Set RS bit
#define ssd1803_SET_RS (ssd1803_REG(9))

// Use extended register
#define ssd1803_SET_RE (ssd1803_REG(10))

// Use special register
#define ssd1803_SET_IS (ssd1803_REG(11))

// Helper macros, for defining registers where RE or IS or both are set.
#define ssd1803_RE_REG(pos) (ssd1803_SET_RE | ssd1803_REG(pos))
#define ssd1803_IS_REG(pos) (ssd1803_SET_IS | ssd1803_REG(pos))
#define ssd1803_IS_RE_REG(pos) (ssd1803_SET_IS | ssd1803_SET_RE | ssd1803_REG(pos))

// read or write a register
#define ssd1803_WRITE_OPERATION 0
#define ssd1803_READ_OPERATION 1

#define ssd1803_SPI_START_BYTE 0xf8

#define ssd1803_CLEAR_DISPLAY ssd1803_REG(0)

#define ssd1803_RETURN_HOME ssd1803_REG(1)

#define ssd1803_POWER_DOWN_MODE ssd1803_RE_REG(1)
#define ssd1803_POWER_DOWN 0

#define ssd1803_ENTRY_MODE_SET_0 ssd1803_REG(2)
#define ssd1803_ENTRY_MODE_SET_0_S 0
#define ssd1803_ENTRY_MODE_SET_0_ID 1

#define ssd1803_ENTRY_MODE_SET_1 ssd1803_RE_REG(2)
#define ssd1803_ENTRY_MODE_SET_1_BDS 0
#define ssd1803_ENTRY_MODE_SET_1_BDC 1

#define ssd1803_DISPLAY_ON_OFF_CONTROL ssd1803_REG(3)
#define ssd1803_DISPLAY_ON_OFF_CONTROL_B 0
#define ssd1803_DISPLAY_ON_OFF_CONTROL_C 1
#define ssd1803_DISPLAY_ON_OFF_CONTROL_D 2

#define ssd1803_EXTENDED_FUNCTION_SET ssd1803_RE_REG(3)
#define ssd1803_EXTENDED_FUNCTION_SET_NW 0
#define ssd1803_EXTENDED_FUNCTION_SET_BW 1
#define ssd1803_EXTENDED_FUNCTION_SET_FW 2

#define ssd1803_CURSOR_OR_DISPLAY_SHIFT ssd1803_REG(4)
#define ssd1803_CURSOR_OR_DISPLAY_SHIFT_RL 2
#define ssd1803_CURSOR_OR_DISPLAY_SHIFT_SC 3

#define ssd1803_DOUBLE_HEIGHT ssd1803_RE_REG(4)
#define ssd1803_DOUBLE_HEIGHT_DH 0
#define ssd1803_DOUBLE_HEIGHT_BS1 1
#define ssd1803_DOUBLE_HEIGHT_UD1 2
#define ssd1803_DOUBLE_HEIGHT_UD2 3

#define ssd1803_INTERNAL_OSC ssd1803_IS_REG(4)
#define ssd1803_INTERNAL_OSC_F0 0
#define ssd1803_INTERNAL_OSC_F1 1
#define ssd1803_INTERNAL_OSC_F2 2
#define ssd1803_INTERNAL_OSC_BS0 3

#define ssd1803_SHIFT_SCROLL_ENABLE ssd1803_IS_RE_REG(4)
#define ssd1803_SHIFT_SCROLL_ENABLE_S1 0
#define ssd1803_SHIFT_SCROLL_ENABLE_S2 1
#define ssd1803_SHIFT_SCROLL_ENABLE_S3 2
#define ssd1803_SHIFT_SCROLL_ENABLE_S4 3

#define ssd1803_FUNCTION_SET_0 ssd1803_REG(5)
#define ssd1803_FUNCTION_SET_0_IS 0
#define ssd1803_FUNCTION_SET_0_RE 1
#define ssd1803_FUNCTION_SET_0_DH 2
#define ssd1803_FUNCTION_SET_0_N 3
#define ssd1803_FUNCTION_SET_0_DL 4

#define ssd1803_FUNCTION_SET_1 ssd1803_RE_REG(5)
#define ssd1803_FUNCTION_SET_1_REV 0
#define ssd1803_FUNCTION_SET_1_RE 1
#define ssd1803_FUNCTION_SET_1_BE 2
#define ssd1803_FUNCTION_SET_1_N 3
#define ssd1803_FUNCTION_SET_1_DL 4

#define ssd1803_SET_CGRAM_ADDRESS ssd1803_REG(6)
#define ssd1803_SET_CGRAM_ADDRESS_AC0 0
#define ssd1803_SET_CGRAM_ADDRESS_AC1 1
#define ssd1803_SET_CGRAM_ADDRESS_AC2 2
#define ssd1803_SET_CGRAM_ADDRESS_AC3 3
#define ssd1803_SET_CGRAM_ADDRESS_AC4 4
#define ssd1803_SET_CGRAM_ADDRESS_AC5 5

#define ssd1803_SET_SEGRAM_ADDRESS ssd1803_IS_REG(6)
#define ssd1803_SET_SEGRAM_ADDRESS_AC0 0
#define ssd1803_SET_SEGRAM_ADDRESS_AC1 1
#define ssd1803_SET_SEGRAM_ADDRESS_AC2 2
#define ssd1803_SET_SEGRAM_ADDRESS_AC3 3

#define ssd1803_POWER_SET (ssd1803_SET_IS | ssd1803_REG(6) | ssd1803_REG(4))
#define ssd1803_POWER_SET_C4 0
#define ssd1803_POWER_SET_C5 1
#define ssd1803_POWER_SET_BON 2
#define ssd1803_POWER_SET_ION 3

#define ssd1803_FOLLOWER_CONTROL (ssd1803_SET_IS | ssd1803_REG(6) | ssd1803_REG(5))
#define ssd1803_FOLLOWER_CONTROL_RAB0 0
#define ssd1803_FOLLOWER_CONTROL_RAB1 1
#define ssd1803_FOLLOWER_CONTROL_RAB2 2
#define ssd1803_FOLLOWER_CONTROL_DON 3

#define ssd1803_CONTRAST_SET (ssd1803_SET_IS | ssd1803_REG(6) | ssd1803_REG(5) | ssd1803_REG(4))
#define ssd1803_CONTRAST_SET_C0 0
#define ssd1803_CONTRAST_SET_C1 1
#define ssd1803_CONTRAST_SET_C2 2
#define ssd1803_CONTRAST_SET_C3 3

#define ssd1803_SET_DDRAM_ADDRESS ssd1803_REG(7)
#define ssd1803_SET_DDRAM_ADDRESS_AC0 0
#define ssd1803_SET_DDRAM_ADDRESS_AC1 1
#define ssd1803_SET_DDRAM_ADDRESS_AC2 2
#define ssd1803_SET_DDRAM_ADDRESS_AC3 3
#define ssd1803_SET_DDRAM_ADDRESS_AC4 4
#define ssd1803_SET_DDRAM_ADDRESS_AC5 5
#define ssd1803_SET_DDRAM_ADDRESS_AC6 6

#define ssd1803_SET_SCROLL_QUANTITY ssd1803_RE_REG(7)
#define ssd1803_SET_SCROLL_QUANTITY_SQ0 0
#define ssd1803_SET_SCROLL_QUANTITY_SQ1 1
#define ssd1803_SET_SCROLL_QUANTITY_SQ2 2
#define ssd1803_SET_SCROLL_QUANTITY_SQ3 3
#define ssd1803_SET_SCROLL_QUANTITY_SQ4 4
#define ssd1803_SET_SCROLL_QUANTITY_SQ5 5

#define ssd1803_TEMPCO_CONTROL (ssd1803_SET_RE | 0x76)
#define ssd1803_TEMPCO_CONTROL_SET ssd1803_SET_RS
#define ssd1803_TEMPCO_CONTROL_SET_TC0 0
#define ssd1803_TEMPCO_CONTROL_SET_TC1 1
#define ssd1803_TEMPCO_CONTROL_SET_TC2 2

#define ssd1803_ROM_SELECT (ssd1803_SET_RE | 0x72)
#define ssd1803_ROM_SELECT_SET ssd1803_SET_RS
#define ssd1803_ROM_SELECT_SET_ROM1 2
#define ssd1803_ROM_SELECT_SET_ROM2 3

#endif